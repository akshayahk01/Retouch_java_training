package Loops_in_java;

public class Whileloop {

    public static void main(String[] args) {

//        increment
        int i=1;
        while (i <=10) {
            System.out.println("hi Akshay welcome " + i);
            i++;
        }

//        decrement
        System.out.println("in reversed format");
        int j =10;
        while (j >=1) {
            System.out.println("hi Akshay welcome to reverse " + j);
            j--;
        }



    }
}
