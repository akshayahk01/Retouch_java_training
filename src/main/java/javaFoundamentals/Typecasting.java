package javaFoundamentals;

public class Typecasting {
    public static void main(String[] args) {
        int a = 100;
        double b = a; // typecasting int to double

        double x = 99.99;
        int y = (int) x; //  typecasting double to int

        System.out.println(b);
        System.out.println(y);
    }
}

