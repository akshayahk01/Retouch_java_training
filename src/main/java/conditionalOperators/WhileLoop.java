package conditionalOperators;

public class WhileLoop {
    public static void main(String[] args) {
//        increment
        int i = 1;
        while (i <= 5) {
            System.out.println("Hi Akshay hk: " + i);
            i++;
        }
// decrement
        int j = 10;
        while (j >= 1) {
            System.out.println("welcome to java: " + j);
            j--;
        }


        }



        }


